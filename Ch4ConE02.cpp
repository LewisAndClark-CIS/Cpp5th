//Increment operator example

#include <iostream>

using std::cout;
using std::endl;

int main()
{
	//declare variable
	int quantity = 10;

	//add 1 to variable
	quantity = quantity + 1;

	//display variable's value
	cout << "New quantity: " << quantity << endl;

	return 0;
}	//end of main function